package org.java.entity;

public class Ward {
    private Integer wardId;

    private Integer wardAdministration;

    private Integer wardNumber;

    private Integer wardPrice;

    private Integer wardState;

    public Integer getWardId() {
        return wardId;
    }

    public void setWardId(Integer wardId) {
        this.wardId = wardId;
    }

    public Integer getWardAdministration() {
        return wardAdministration;
    }

    public void setWardAdministration(Integer wardAdministration) {
        this.wardAdministration = wardAdministration;
    }

    public Integer getWardNumber() {
        return wardNumber;
    }

    public void setWardNumber(Integer wardNumber) {
        this.wardNumber = wardNumber;
    }

    public Integer getWardPrice() {
        return wardPrice;
    }

    public void setWardPrice(Integer wardPrice) {
        this.wardPrice = wardPrice;
    }

    public Integer getWardState() {
        return wardState;
    }

    public void setWardState(Integer wardState) {
        this.wardState = wardState;
    }
}