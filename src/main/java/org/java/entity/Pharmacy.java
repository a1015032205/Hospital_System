package org.java.entity;

public class Pharmacy {
    private Integer pharmacyId;

    private Integer pharmacyAdministration;

    private Integer pharmacyNumber;

    public Integer getPharmacyId() {
        return pharmacyId;
    }

    public void setPharmacyId(Integer pharmacyId) {
        this.pharmacyId = pharmacyId;
    }

    public Integer getPharmacyAdministration() {
        return pharmacyAdministration;
    }

    public void setPharmacyAdministration(Integer pharmacyAdministration) {
        this.pharmacyAdministration = pharmacyAdministration;
    }

    public Integer getPharmacyNumber() {
        return pharmacyNumber;
    }

    public void setPharmacyNumber(Integer pharmacyNumber) {
        this.pharmacyNumber = pharmacyNumber;
    }
}